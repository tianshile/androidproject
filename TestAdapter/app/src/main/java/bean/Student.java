public class Student {
    private int id;
    private String name;
    private String idtentify;
    private String sex;
    private int age;
    private int tuxiang;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIdtentify() {
        return idtentify;
    }

    public void setIdtentify(String idtentify) {
        this.idtentify = idtentify;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getTuxiang() {
        return tuxiang;
    }

    public void setTuxiang(int tuxiang) {
        this.tuxiang = tuxiang;
    }
}
